                    <p>5.4.1 This is how you take a screenshot in Windows:</p>
                    <p>Step 1: Make sure that the current view on the screen is what you want to take a screenshot of.</p>
                    <p>Step 2: ress “Print Screen” on the keyboard (this button is usually on the right hand side of the keyboard).</p>
                    <p>Step 3: Open the program “Paint” or “Word” on your computer, and then use short command Ctrl + V (or choose Paste from the Edit menu). An image will be pasted ( this is the image that the computer saved when you clicked the “Print Screen” key).</p>
